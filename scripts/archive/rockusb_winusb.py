# Helpers
# ---------------------------------------------------------------------------
def _last_err(prefix):
    e = ctypes.GetLastError()
    return OSError(f'{prefix} failed: GetLastError={e}')


def list_device_paths(guid_str):
    """Yield all device paths registered under the given DeviceInterfaceGUID."""
    guid = parse_guid(guid_str)
    h = setupapi.SetupDiGetClassDevsW(byref(guid), None, None, DIGCF_PRESENT | DIGCF_DEVICEINTERFACE)
    if h == INVALID_HANDLE_VALUE:
        raise _last_err('SetupDiGetClassDevs')
    try:
        idx = 0
        while True:
            iface = SP_DEVICE_INTERFACE_DATA()
            iface.cbSize = sizeof(SP_DEVICE_INTERFACE_DATA)
            if not setupapi.SetupDiEnumDeviceInterfaces(h, None, byref(guid), idx, byref(iface)):
                err = ctypes.GetLastError()
                if err == ERROR_NO_MORE_ITEMS:
                    break
                raise OSError(f'SetupDiEnumDeviceInterfaces[{idx}] failed: {err}')

            detail = SP_DEVICE_INTERFACE_DETAIL_DATA_W()
            # On 64-bit Windows, the documented cbSize of this struct is 8.
            # On 32-bit, it's 6. The struct is variable-length but we set
            # cbSize to the fixed-header size, NOT sizeof(detail).
            detail.cbSize = 8 if sizeof(c_void_p) == 8 else 6
            req = c_ulong()
            ok = setupapi.SetupDiGetDeviceInterfaceDetailW(
                h, byref(iface), byref(detail), sizeof(detail), byref(req), None
            )
            if not ok:
                raise _last_err('SetupDiGetDeviceInterfaceDetail')
            yield detail.DevicePath
            idx += 1
    finally:
        setupapi.SetupDiDestroyDeviceInfoList(h)


class WinUsbDevice:
    def __init__(self, device_path, pipe_timeout_ms=4000):
        self.device_path = device_path
        self.h = kernel32.CreateFileW(
            device_path,
            GENERIC_READ | GENERIC_WRITE,
            FILE_SHARE_READ | FILE_SHARE_WRITE,
            None,
            OPEN_EXISTING,
            FILE_FLAG_OVERLAPPED,
            None,
        )
        if self.h == INVALID_HANDLE_VALUE:
            raise _last_err('CreateFile')
        self.winusb = c_void_p()
        if not winusb.WinUsb_Initialize(self.h, byref(self.winusb)):
            kernel32.CloseHandle(self.h)
            raise _last_err('WinUsb_Initialize')
        # Set per-pipe timeout so reads don't hang forever if the device
        # never responds. WinUSB default is "wait forever".
        timeout = c_ulong(pipe_timeout_ms)
        for ep in (EP_OUT, EP_IN):
            if not winusb.WinUsb_SetPipePolicy(self.winusb, ep,
                                                PIPE_TRANSFER_TIMEOUT,
                                                sizeof(timeout), byref(timeout)):
                # not fatal - just print a warning
                err = ctypes.GetLastError()
                print(f'  (SetPipePolicy(ep=0x{ep:02x}, TIMEOUT={pipe_timeout_ms}ms) failed: {err})')

    def reset_pipes(self):
        for ep in (EP_OUT, EP_IN):
            winusb.WinUsb_ResetPipe(self.winusb, ep)

    def msc_bulk_reset(self, interface_num=1):
        """Issue USB Mass Storage Class Bulk-Only Reset on the control pipe.
        bmRequestType=0x21, bRequest=0xFF, wValue=0, wIndex=interface, wLength=0."""
        # WINUSB_SETUP_PACKET as packed uint64 (LE):
        #   byte 0: RequestType
        #   byte 1: Request
        #   bytes 2..3: Value (LE)
        #   bytes 4..5: Index (LE)
        #   bytes 6..7: Length (LE)
        setup = struct.pack('<BBHHH', 0x21, 0xFF, 0, interface_num, 0)
        setup_int = struct.unpack('<Q', setup)[0]
        transferred = c_ulong()
        ok = winusb.WinUsb_ControlTransfer(self.winusb, setup_int, None, 0, byref(transferred), None)
        if not ok:
            err = ctypes.GetLastError()
            print(f'  (msc_bulk_reset control transfer failed: {err})')
            return False
        return True

    def close(self):
        if self.winusb:
            winusb.WinUsb_Free(self.winusb)
            self.winusb = c_void_p()
        if self.h:
            kernel32.CloseHandle(self.h)
            self.h = None

    def write_pipe(self, ep, data):
        transferred = c_ulong()
        buf = (c_ubyte * len(data)).from_buffer_copy(data)
        if not winusb.WinUsb_WritePipe(self.winusb, ep, buf, len(data), byref(transferred), None):
            raise _last_err(f'WinUsb_WritePipe(ep=0x{ep:02x}, len={len(data)})')
        return transferred.value

    def read_pipe(self, ep, length):
        transferred = c_ulong()
        buf = (c_ubyte * length)()
        if not winusb.WinUsb_ReadPipe(self.winusb, ep, buf, length, byref(transferred), None):
            raise _last_err(f'WinUsb_ReadPipe(ep=0x{ep:02x}, len={length})')
        return bytes(buf[:transferred.value])


# ---------------------------------------------------------------------------
# Rockusb protocol
# ---------------------------------------------------------------------------
class Rockusb:
    def __init__(self, dev: WinUsbDevice):
        self.dev = dev
        self._tag = 1

    def _next_tag(self):
        t = self._tag
        self._tag = (self._tag + 1) & 0xffffffff
        return t

    def cmd(self, opcode, data_xfer_len=0, data_in=True, params_be=b''):
        tag = self._next_tag()
        flags = 0x80 if data_in else 0x00
        cbwcb = bytes([opcode]) + params_be
        if len(cbwcb) > 16:
            raise RuntimeError(f'CBWCB too long: {len(cbwcb)}')
        cmd_len = len(cbwcb)
        cbwcb = cbwcb.ljust(16, b'\x00')

        cbw = struct.pack('<IIIBBB', CBW_SIGNATURE, tag, data_xfer_len, flags, 0, cmd_len) + cbwcb
        assert len(cbw) == 31

        self.dev.write_pipe(EP_OUT, cbw)

        data = b''
        if data_xfer_len > 0:
            if data_in:
                data = self.dev.read_pipe(EP_IN, data_xfer_len)
            else:
                # OUT data phase not implemented in cmd(); use directly
                raise NotImplementedError

        csw = self.dev.read_pipe(EP_IN, 13)
        if len(csw) != 13:
            raise RuntimeError(f'CSW size wrong: {len(csw)}')
        sig, rsp_tag, residue, status = struct.unpack('<IIIB', csw)
        if sig != CSW_SIGNATURE:
            raise RuntimeError(f'CSW signature wrong: 0x{sig:08x}')
        if rsp_tag != tag:
            raise RuntimeError(f'CSW tag mismatch: got {rsp_tag} expected {tag}')
        return status, residue, data


