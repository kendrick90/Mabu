"""
rockusb_winusb.py - Rockchip rockusb client using direct Win32 WinUSB API.

Sidesteps libusb-on-Windows compositey-ness: we have WinUSB bound to MI_01
of the recovery composite device via Zadig, and we just open it directly
through SetupAPI + WinUSB.

The MI_01 interface's DeviceInterfaceGUID is randomly generated by Zadig
(passed in via command line - default is the GUID Zadig set on this host).
Endpoint addresses are hardcoded to what `rockusb.py probe` showed:
  OUT bulk: 0x04
  IN  bulk: 0x83

CLI:
    python rockusb_winusb.py probe
    python rockusb_winusb.py info
    python rockusb_winusb.py read-lba <start> <count> <out.bin>
    python rockusb_winusb.py reset
"""

import sys
import struct
import ctypes
import ctypes.wintypes as wt
from ctypes import (
    POINTER, byref, sizeof, Structure, c_void_p, c_ulong, c_byte, c_ubyte,
    c_ushort, c_uint, c_wchar_p, c_int,
)

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------
# Zadig-assigned DeviceInterfaceGUID for MI_01 on this host. The script will
# also accept --guid on the command line if it ever changes.
DEFAULT_GUID = '{69800760-AD24-4938-B341-49FEFE9C9C7C}'

EP_OUT = 0x04
EP_IN  = 0x83

DIGCF_PRESENT          = 0x02
DIGCF_DEVICEINTERFACE  = 0x10
GENERIC_READ           = 0x80000000
GENERIC_WRITE          = 0x40000000
FILE_SHARE_READ        = 1
FILE_SHARE_WRITE       = 2
OPEN_EXISTING          = 3
FILE_FLAG_OVERLAPPED   = 0x40000000
ERROR_NO_MORE_ITEMS    = 259
ERROR_INSUFFICIENT_BUFFER = 122
INVALID_HANDLE_VALUE   = c_void_p(-1).value

# Rockchip CBW opcodes
CMD_TEST_UNIT_READY  = 0x00
CMD_READ_FLASH_ID    = 0x01
CMD_READ_LBA         = 0x14
CMD_WRITE_LBA        = 0x15
CMD_READ_FLASH_INFO  = 0x1A
CMD_READ_CHIP_INFO   = 0x1B
CMD_READ_CAPABILITY  = 0x1D
CMD_DEVICE_RESET     = 0xFF

CBW_SIGNATURE = 0x43425355
CSW_SIGNATURE = 0x53425355

# ---------------------------------------------------------------------------
# Win32 ctypes
# ---------------------------------------------------------------------------
setupapi = ctypes.windll.setupapi
kernel32 = ctypes.windll.kernel32
winusb   = ctypes.windll.winusb

class GUID(Structure):
    _fields_ = [
        ('Data1', c_ulong),
        ('Data2', c_ushort),
        ('Data3', c_ushort),
        ('Data4', c_byte * 8),
    ]

def parse_guid(s):
    s = s.strip().strip('{}').lower()
    parts = s.split('-')
    g = GUID()
    g.Data1 = int(parts[0], 16)
    g.Data2 = int(parts[1], 16)
    g.Data3 = int(parts[2], 16)
    d4 = bytes.fromhex(parts[3]) + bytes.fromhex(parts[4])
    for i, b in enumerate(d4):
        g.Data4[i] = b
    return g

class SP_DEVICE_INTERFACE_DATA(Structure):
    _fields_ = [
        ('cbSize', c_ulong),
        ('InterfaceClassGuid', GUID),
        ('Flags', c_ulong),
        ('Reserved', c_void_p),
    ]

# SP_DEVICE_INTERFACE_DETAIL_DATA_W has variable-length DevicePath. Use a
# generous fixed buffer.
class SP_DEVICE_INTERFACE_DETAIL_DATA_W(Structure):
    _fields_ = [
        ('cbSize', c_ulong),
        ('DevicePath', wt.WCHAR * 1024),
    ]

