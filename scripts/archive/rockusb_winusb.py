def hex_dump(data, prefix='  '):
    if not data:
        return ''
    out = []
    for i in range(0, len(data), 16):
        chunk = data[i:i+16]
        hx = ' '.join(f'{b:02x}' for b in chunk)
        ascii_repr = ''.join(chr(b) if 32 <= b < 127 else '.' for b in chunk)
        out.append(f'{prefix}{i:04x}  {hx:<47s}  {ascii_repr}')
    return '\n'.join(out)


def open_first(guid_str):
    paths = list(list_device_paths(guid_str))
    if not paths:
        raise RuntimeError(f'No device with DeviceInterfaceGUID {guid_str} found.')
    if len(paths) > 1:
        print(f'Multiple devices with that GUID, using first:')
        for p in paths: print(f'  {p}')
    return WinUsbDevice(paths[0])


def cmd_probe(guid):
    paths = list(list_device_paths(guid))
    print(f'DeviceInterfaceGUID {guid}:')
    if not paths:
        print('  (no devices)')
        return
    for p in paths:
        print(f'  path: {p}')
    # Try opening
    dev = WinUsbDevice(paths[0])
    print(f'Opened OK: handle={dev.h} winusb_handle={dev.winusb}')
    dev.close()


def cmd_info(guid):
    dev = open_first(guid)
    # Some rockusb gadgets need a Mass-Storage Bulk Reset before the first CBW.
    print('Sending USB MSC Bulk Reset...')
    dev.msc_bulk_reset(interface_num=1)
    dev.reset_pipes()
    rk = Rockusb(dev)
    try:
        print('TestUnitReady...')
        s, _, _ = rk.cmd(CMD_TEST_UNIT_READY, 0)
        print(f'TestUnitReady: status={s}')

        s, _, d = rk.cmd(CMD_READ_CAPABILITY, 8)
        print(f'ReadCapability: status={s}')
        print(hex_dump(d))

        s, _, d = rk.cmd(CMD_READ_FLASH_ID, 5)
        print(f'ReadFlashID: status={s}')
        print(hex_dump(d))

        s, _, d = rk.cmd(CMD_READ_FLASH_INFO, 11)
        print(f'ReadFlashInfo: status={s}')
        print(hex_dump(d))
        if d and len(d) >= 4:
            sectors = struct.unpack('<I', d[:4])[0]
            print(f'  -> flash size: {sectors} sectors = {sectors * 512 / (1<<30):.2f} GiB')

        s, _, d = rk.cmd(CMD_READ_CHIP_INFO, 16)
        print(f'ReadChipInfo: status={s}')
        print(hex_dump(d))
        # ASCII chip name?
        ascii_name = ''.join(chr(b) for b in d if 32 <= b < 127).strip()
        if ascii_name:
            print(f'  -> ASCII tag: {ascii_name!r}')
    finally:
        dev.close()


def cmd_read_lba(guid, args):
    if len(args) != 3:
        print('Usage: read-lba <start_sector> <count> <out.bin>')
        sys.exit(1)
    start = int(args[0], 0); count = int(args[1], 0); out = args[2]
    dev = open_first(guid)
    rk = Rockusb(dev)
    try:
        params = struct.pack('>IH', start, count) + b'\x00\x00'
        s, _, d = rk.cmd(CMD_READ_LBA, count * 512, data_in=True, params_be=params)
        print(f'ReadLBA(start={start}, count={count}): status={s}, got {len(d)} bytes')
        if d:
            with open(out, 'wb') as f:
                f.write(d)
            print(f'Wrote {len(d)} bytes to {out}')
    finally:
        dev.close()


def main():
    args = sys.argv[1:]
    guid = DEFAULT_GUID
    if args and args[0] == '--guid':
        guid = args[1]; args = args[2:]
    if not args:
        print(__doc__); sys.exit(1)
    cmd, *rest = args
    if cmd == 'probe':       cmd_probe(guid)
    elif cmd == 'info':      cmd_info(guid)
    elif cmd == 'read-lba':  cmd_read_lba(guid, rest)
    elif cmd == 'reset':
        dev = open_first(guid)
        Rockusb(dev).cmd(CMD_DEVICE_RESET, 0)
        dev.close()
        print('Reset sent')
    else:
        print(f'Unknown command: {cmd}'); sys.exit(1)

if __name__ == '__main__':
    main()
